    <h1>Receita de Fricassê de Frango</h1>
    <p>Sente falta de um tempero de vó na sua comida? Se sim, acompanhe a receita abaixo!</p>
    <h2>Ingredientes:</h2>
    <ul><li>1 peito de frango cozido e desfiado</li></ul>
    <ul><li>1 lata de milho verde escorrido</li></ul>
    <ul><li>1 caixa de creme de leite</li></ul>
    <ul><li>1 copo de requeijão cremoso</li></ul>
    <ul><li>1 xícara de água do cozimento do frango</li></ul>
    <ul><li>2 colheres (sopa) de azeite ou manteiga</li></ul>
    <ul><li>1 cebola pequena picada</li></ul>
    <ul><li>2 dentes de alho picados</li></ul>
    <ul><li>200 g de queijo muçarela fatiado</li></ul>
    <ul><li>Batata palha a gosto</li></ul>
    <ul><li>Sal e pimenta a gosto</li></ul>
    <h2>Modo de Preparo</h2>
    <ol>
    <li>No liquidificador, bata o milho, o creme de leite, o requeijão e a água do cozimento do frango até formar um creme homogêneo.</li>
    <li>Em uma panela, aqueça o azeite, refogue a cebola e o alho.</li>
    <li>Acrescente o frango desfiado, tempere com sal e pimenta e misture bem.</li>
    <li>Adicione o creme batido e cozinhe em fogo baixo por 5 minutos, mexendo sempre.</li>
    <li>Transfira a mistura para um refratário, cubra com o queijo muçarela e leve ao forno preaquecido a 200 °C até gratinar.</li>
    <li>Finalize com batata palha por cima antes de servir.</li>
    </ol>
